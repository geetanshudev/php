<?php

/*in this video we have to see the switch case of php
in which we have multiple statements but we have to choose any one of them */

$var=17;
switch($var){
	case 15:
		echo"number 15";
		break;
	case 16:
		echo"number 16";
		break;	
		
	case 17:
		echo"number 17";
		break;
		
	default :
		echo "default statement";
		
}
?>